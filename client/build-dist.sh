#!/bin/bash

# سكريبت بناء الواجهة الأمامية (client)
echo "🛠️ بدء بناء الواجهة الأمامية..."

# إنشاء مجلد dist إذا لم يكن موجوداً
mkdir -p dist

# تنفيذ أمر البناء
echo "⚙️ تنفيذ أمر البناء..."
npx vite build

# التحقق من نجاح عملية البناء
if [ $? -eq 0 ]; then
    echo "✅ تم بناء الواجهة الأمامية بنجاح! الملفات موجودة في dist/"
    # عرض قائمة الملفات التي تم إنشاؤها
    echo "📄 قائمة الملفات المنشأة:"
    ls -la dist/
else
    echo "❌ فشل في بناء الواجهة الأمامية"
fi